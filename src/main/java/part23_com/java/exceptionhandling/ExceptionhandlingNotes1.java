package part23_com.java.exceptionhandling;

public class ExceptionhandlingNotes1 {
	/*The exception handling in java is one of the powerful mechanism 
	to handle the runtime errors so that normal flow of the application 
	can be maintained.*/
	
	/*What is exception?
	--------------------
	Exception is an abnormal condition.
	In java, exception is an event that disrupts the normal flow 
	of the program. 
	It is an object which is thrown at runtime.

	What is exception handling?
	Exception Handling is a mechanism to handle runtime errors 
	such as ClassNotFound, IO, SQL, Remote etc.*/
	
	/*SHOW DIAGRAM
	------------*/
	
	/*Types of Exception
	 * -----------------

	There are mainly two types of exceptions: 
		
	1.Checked exceptions and 
	2.Unchecked exceptions*/
	
	/*1.Checked exceptions are checked at compile-time. E.g.IOException, SQLException etc.
	2.Unchecked exceptions are not checked at compile-time rather they are checked at runtime.
	E.g:- ArithmeticException, NullPointerException, ArrayIndexOutOfBoundsException */
	
	
}
