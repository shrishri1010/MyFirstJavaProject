package part_2.com.java.operators;

public class Operator1_Arithmetic {

	public static void main(String[] args) {
		int a=50;
		int b=10;
		
		//Arithmetic operators  ------>>> +  -  *  /   %  
		//Operands ------->>> a and b are operands
		System.out.println(a+b);
		System.out.println("sum of a and b ---->>>"+(a+b));      // concatenation using +
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);    /// returns quotient
		System.out.println(a%b);  // returns reminder
	}
}
