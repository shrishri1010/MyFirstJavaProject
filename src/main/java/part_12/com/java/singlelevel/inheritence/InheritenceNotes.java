package part_12.com.java.singlelevel.inheritence;

public class InheritenceNotes {

	 /*Inheritance in Java
	 ---------------------
	 Inheritance in java is a mechanism in which one object acquires 
	 all the properties and behaviors of parent object.
	
	 The idea behind inheritance in java is that,
	 you can create new classes that are built 
	 upon existing classes. When you inherit from an existing class, 
	 you can reuse methods and fields of parent class, 
	 and you can add new methods and fields also.*/
	
	/*Why use inheritance in java
	 * --------------------------

	For Method Overriding (so runtime polymorphism can be achieved).
	For Code Reusability.*/

	/*Syntax of Java Inheritance using extends Keyword
	 * -----------------------------------------------
	class Subclass-name extends Superclass-name  
	{  
	   //methods and fields  
	}  */
	
	/*Types of inheritance in java
	------------------------------
	On the basis of class, there can be three types of inheritance in java:
	single, multilevel.*/
	
}
