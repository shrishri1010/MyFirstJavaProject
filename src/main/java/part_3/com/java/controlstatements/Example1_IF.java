public class Example1_IF {
	//when to use if .... else -  when  the condition is less than 2 conditions.
	
	public static void main(String args[]) {
		int i=50;
		
		if(i>100){
			System.out.println("50 is greater than 100");
		}
		else {
			System.out.println("50 is less than 100");
		}
	}
}
