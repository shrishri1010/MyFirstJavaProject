package part_11.com.java.polymorphism;

public class MethodOverRidingNotes {
	/**
	 * If subclass (child class) has the same method as declared in the parent class, 
	 * it is known as method overriding in java.
	 * It is also known as "Run Time Polymorphism".
	 */
	
	/**
	 * Usage of Java Method Overriding
	 * -------------------------------
   
		Method overriding is used to provide specific implementation 
		of a method 
		that is already provided by its super class.
		Method overriding is used for runtime polymorphism.
		
		Rules for Java Method Overriding
		--------------------------------
		1. Method must have same name as in the parent class.
		2. Method must have same parameter as in the parent class.
		3. In Method Overriding, method definition/method signature should be same but body of the method should be changed.
	 */
}
