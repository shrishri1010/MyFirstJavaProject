package part_8.com.java.polymorphism;

public class MethodOverloadingNotes {
	/*If a class has multiple methods having same name(name of the method should be same) 
	 * but different in parameters, 
	It is known as Method Overloading or Compile Time Polymorphism */
	
	/*Advantage of method overloading
	---------------------------------------
	1. Method overloading increases the readability of the program.
	2. Saves the memory
	3. Saves time

	Different ways to overload the method
	------------------------------------------
	1.By changing number of arguments/parameters
	2.By changing the return type of the arguments/parameters
	*/
}
